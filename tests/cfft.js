function zeros(n) {
    return Array(n).fill(0)
}
function ones(n) {
    return Array(n).fill(1)
}
function imp(n, k=0) {
    let v = zeros(n)
    v[k] = 1
    return v
}
function drange(n) {
    let out = []
    for(let i = 1; i <= n; i++) {
        out.push(i)
    }
    return out
}
function c_zeros(n) {
    return Array(n*2).fill(0)
}
function c_ones(n) {
    let v = c_zeros(n)
    for(let i = 0; i < n * 2; i += 2) {
        v[i] = 1
    }
    return v
}
