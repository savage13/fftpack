// FFT(IFFT(x)) = x 
for(let n = 1; n <= 64; n++) {
    // Impulse function at all points
    for(let k = 0; k < n; k++) {
        let input = imp(n, k)
        let tmp = fft(input)
        let output = ifft(tmp).filter((v,i) => i % 2 == 0)
        eq(input, output)
    }
    {
        // Linear Ramp
        let input = drange(n)
        let tmp = fft(input)
        let output = ifft(tmp).filter((v,i) => i % 2 == 0)
        eq(input, output)
    }
    {
        // Negative Ramp
        let input = drange(n).map(v => -v)
        let tmp = fft(input)
        let output = ifft(tmp).filter((v,i) => i % 2 == 0)
        eq(input, output)
    }
    {
        // Inverse Linear Ramp
        let input = drange(n).toReversed()
        let tmp = fft(input)
        let output = ifft(tmp).filter((v,i) => i % 2 == 0)
        eq(input, output)
    }
    {
        // Random Values [-1,1]
        let input = Array(n).fill(0).map(_ => Math.random() * 2 - 1)
        let tmp = fft(input)
        let output = ifft(tmp).filter((v,i) => i % 2 == 0)
        eq(input, output)
    }
    {
        // Random Values [-10,10]
        let input = Array(n).fill(0).map(_ => 10 * (Math.random() * 2 - 1))
        let tmp = fft(input)
        let output = ifft(tmp).filter((v,i) => i % 2 == 0)
        eq(input, output)
    }
    {
        // Random Values [-pi,pi]
        let input = Array(n).fill(0).map(_ => Math.PI * (Math.random() * 2 - 1))
        let tmp = fft(input)
        let output = ifft(tmp).filter((v,i) => i % 2 == 0)
        eq(input, output)
    }
}


